package egovframework.fusion.cmmn.vo;

import java.util.Date;

public class CommonCodeVO {
	
	private String oriCdCode;
	
	private String cdCode;
	
	private String gpCode;
	
	private String cdNm;
	
	private String cdDesc;
	
	private Integer cdOrder;
	
	private Date cdRegDate;
	
	private Date cdModDate;
	
	private String cdDelyn;
	
	public String getOriCdCode() {
		return oriCdCode;
	}

	public void setOriCdCode(String oriCdCode) {
		this.oriCdCode = oriCdCode;
	}

	public String getCdCode() {
		return cdCode;
	}

	public void setCdCode(String cdCode) {
		this.cdCode = cdCode;
	}

	public String getGpCode() {
		return gpCode;
	}

	public void setGpCode(String gpCode) {
		this.gpCode = gpCode;
	}

	public String getCdNm() {
		return cdNm;
	}

	public void setCdNm(String cdNm) {
		this.cdNm = cdNm;
	}

	public String getCdDesc() {
		return cdDesc;
	}

	public void setCdDesc(String cdDesc) {
		this.cdDesc = cdDesc;
	}

	public Integer getCdOrder() {
		return cdOrder;
	}

	public void setCdOrder(Integer cdOrder) {
		this.cdOrder = cdOrder;
	}

	public Date getCdRegDate() {
		return cdRegDate;
	}

	public void setCdRegDate(Date cdRegDate) {
		this.cdRegDate = cdRegDate;
	}

	public Date getCdModDate() {
		return cdModDate;
	}

	public void setCdModDate(Date cdModDate) {
		this.cdModDate = cdModDate;
	}

	public String getCdDelyn() {
		return cdDelyn;
	}

	public void setCdDelyn(String cdDelyn) {
		this.cdDelyn = cdDelyn;
	}
	
}
