package egovframework.fusion.manage.vo;

public class Menu {
	
	private Integer menuNo;
	
	private String menuNm;
	
	private String menuPath;
	
	private Integer menuOrder;
	
	private String menuDelyn;
	
	private String menuIcon;

	private String menuType; 
	
	private String menuParameter;
	
	private String menuTarget;
	
	public Integer getMenuNo() {
		return menuNo;
	}

	public void setMenuNo(Integer menuNo) {
		this.menuNo = menuNo;
	}

	public String getMenuNm() {
		return menuNm;
	}

	public void setMenuNm(String menuNm) {
		this.menuNm = menuNm;
	}

	public String getMenuPath() {
		return menuPath;
	}

	public void setMenuPath(String menuPath) {
		this.menuPath = menuPath;
	}

	public Integer getMenuOrder() {
		return menuOrder;
	}

	public void setMenuOrder(Integer menuOrder) {
		this.menuOrder = menuOrder;
	}

	public String getMenuDelyn() {
		return menuDelyn;
	}

	public void setMenuDelyn(String menuDelyn) {
		this.menuDelyn = menuDelyn;
	}

	public String getMenuIcon() {
		return menuIcon;
	}

	public void setMenuIcon(String menuIcon) {
		this.menuIcon = menuIcon;
	}

	public String getMenuType() {
		return menuType;
	}

	public void setMenuType(String menuType) {
		this.menuType = menuType;
	}

	public String getMenuParameter() {
		return menuParameter;
	}

	public void setMenuParameter(String menuParameter) {
		this.menuParameter = menuParameter;
	}

	public String getMenuTarget() {
		return menuTarget;
	}

	public void setMenuTarget(String menuTarget) {
		this.menuTarget = menuTarget;
	}
	
}
