package egovframework.fusion.manage.vo;

import java.util.Date;

public class MenuLog {
	
	private Integer menuLogNo;
	
	private Integer menuNo;
	
	private Date menuLogTime;

	public Integer getMenuLogNo() {
		return menuLogNo;
	}

	public void setMenuLogNo(Integer menuLogNo) {
		this.menuLogNo = menuLogNo;
	}

	public Integer getMenuNo() {
		return menuNo;
	}

	public void setMenuNo(Integer menuNo) {
		this.menuNo = menuNo;
	}

	public Date getMenuLogTime() {
		return menuLogTime;
	}

	public void setMenuLogTime(Date menuLogTime) {
		this.menuLogTime = menuLogTime;
	}
	
	

}
