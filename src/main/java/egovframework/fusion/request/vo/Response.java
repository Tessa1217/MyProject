package egovframework.fusion.request.vo;

import java.util.Date;

public class Response {
	
	private Integer respNo;
	
	private Integer responserNo;
	
	private Integer reqNo;
	
	private String respContent;
	
	private String respRegDate;
	
	private String respModDate;
	
	private Integer respDegree;
	
	private String respType;

	public Integer getRespNo() {
		return respNo;
	}

	public void setRespNo(Integer respNo) {
		this.respNo = respNo;
	}

	public Integer getResponserNo() {
		return responserNo;
	}

	public void setResponserNo(Integer responserNo) {
		this.responserNo = responserNo;
	}

	public Integer getReqNo() {
		return reqNo;
	}

	public void setReqNo(Integer reqNo) {
		this.reqNo = reqNo;
	}

	public String getRespContent() {
		return respContent;
	}

	public void setRespContent(String respContent) {
		this.respContent = respContent;
	}
	
	public String getRespRegDate() {
		return respRegDate;
	}

	public void setRespRegDate(String respRegDate) {
		this.respRegDate = respRegDate;
	}

	public String getRespModDate() {
		return respModDate;
	}

	public void setRespModDate(String respModDate) {
		this.respModDate = respModDate;
	}
	
	public Integer getRespDegree() {
		return respDegree;
	}

	public void setRespDegree(Integer respDegree) {
		this.respDegree = respDegree;
	}

	public String getRespType() {
		return respType;
	}

	public void setRespType(String respType) {
		this.respType = respType;
	}
	
}
