package egovframework.fusion.reserve.vo;

public class RoomSeat {
	
	private Integer roomNo;
	
	private Integer seatNo;
	
	private String seatLoc;
	
	private String seatAvailyn;

	public Integer getRoomNo() {
		return roomNo;
	}

	public void setRoomNo(Integer roomNo) {
		this.roomNo = roomNo;
	}

	public Integer getSeatNo() {
		return seatNo;
	}

	public void setSeatNo(Integer seatNo) {
		this.seatNo = seatNo;
	}
	
	public String getSeatLoc() {
		return seatLoc;
	}

	public void setSeatLoc(String seatLoc) {
		this.seatLoc = seatLoc;
	}

	public String getSeatAvailyn() {
		return seatAvailyn;
	}

	public void setSeatAvailyn(String seatAvailyn) {
		this.seatAvailyn = seatAvailyn;
	}
	
}
