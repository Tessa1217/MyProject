package egovframework.fusion.reserve.vo;

public class StudyRoom {
	
	private Integer roomNo;
	
	private String roomNm;
	
	private String roomUseyn;
	
	private String roomOpenTime;
	
	private String roomCloseTime;

	public Integer getRoomNo() {
		return roomNo;
	}

	public void setRoomNo(Integer roomNo) {
		this.roomNo = roomNo;
	}

	public String getRoomNm() {
		return roomNm;
	}

	public void setRoomNm(String roomNm) {
		this.roomNm = roomNm;
	}

	public String getRoomUseyn() {
		return roomUseyn;
	}

	public void setRoomUseyn(String roomUseyn) {
		this.roomUseyn = roomUseyn;
	}

	public String getRoomOpenTime() {
		return roomOpenTime;
	}

	public void setRoomOpenTime(String roomOpenTime) {
		this.roomOpenTime = roomOpenTime;
	}

	public String getRoomCloseTime() {
		return roomCloseTime;
	}

	public void setRoomCloseTime(String roomCloseTime) {
		this.roomCloseTime = roomCloseTime;
	}
	
}
